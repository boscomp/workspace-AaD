package ej1.dao;

public class PeliculasServicesException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public PeliculasServicesException() {
	}

	public PeliculasServicesException(String message) {
		super(message);

	}

	public PeliculasServicesException(Throwable cause) {
		super(cause);

	}

	public PeliculasServicesException(String message, Throwable cause) {
		super(message, cause);

	}

	public PeliculasServicesException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
